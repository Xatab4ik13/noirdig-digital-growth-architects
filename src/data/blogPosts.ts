// Функция получения поста по slug
export const getPostBySlug = (slug: string): BlogPost | undefined => {
  return blogPosts.find((post) => post.slug === slug);
};

// Функция получения связанных постов
export const getRelatedPosts = (slugs: string[]): BlogPost[] => {
  return blogPosts.filter((post) => slugs.includes(post.slug));
};

// Функция фильтрации по категории
export const getPostsByCategory = (category: string): BlogPost[] => {
  if (category === "Все") return blogPosts;
  return blogPosts.filter((post) => post.category === category);
};
